// Return a random word and its category hint
export function getRandomWord(): { word: string; categoryHint: string } {
    const categoryKeys = Object.keys(CATEGORIES);
    const randomCategoryKey = categoryKeys[Math.floor(Math.random() * categoryKeys.length)];
    const category = CATEGORIES[randomCategoryKey];

    const randomWord = category.words[Math.floor(Math.random() * category.words.length)];

    return {
        word: randomWord,
        categoryHint: category.name
    };
}
